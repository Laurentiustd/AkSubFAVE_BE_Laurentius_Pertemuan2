     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        mahasiswa::create([
            'nim'=>$request->nim,
            'nama'=>$request->name,
            'alamat'=>$request->address,
            'birthdate'=>$request->date
        ]);

        return redirect('/');
    }

